def generate_image(prompt, output_path, size=DEFAULT_SIZE, retries=MAX_RETRIES):
    url = f"{API_BASE}/images/generations"
    headers = {"Content-Type": "application/json", "Authorization": f"Bearer {API_KEY}"}
    payload = {"model": MODEL, "prompt": prompt, "size": size, "n": 1}

    for attempt in range(1, retries + 1):
        try:
            log(f"  请求... (尝试 {attempt}/{retries})")
            resp = requests.post(url, headers=headers, json=payload, timeout=TIMEOUT)
            log(f"  状态码: {resp.status_code}")
            resp.raise_for_status()
            data = resp.json()
            image_url = data.get("data", [{}])[0].get("url") or data.get("url") or data.get("image_url")
            if not image_url:
                log(f"  响应无URL: {json.dumps(data, ensure_ascii=False)[:200]}")
                continue
            log(f"  下载图片...")
            img_resp = requests.get(image_url, timeout=120)
            img_resp.raise_for_status()
            output_path.parent.mkdir(parents=True, exist_ok=True)
            output_path.write_bytes(img_resp.content)
            file_size = output_path.stat().st_size
            if file_size < 1024:
                log(f"  文件过小 ({file_size} bytes)")
                continue
            log(f"  成功! ({file_size / 1024:.1f} KB)")
            return True
        except Exception as e:
            log(f"  失败: {e}")
            if attempt < retries:
                wait = 2 ** attempt
                log(f"  等待 {wait}s...")
                time.sleep(wait)
            else:
                log(f"  重试耗尽")
    return False

def main():
    log("=" * 50)
    log("香辣土豆鸡翅 — 批量生成图片")
    log(f"共 {len(IMAGES)} 张")
    log("=" * 50)

    if not API_KEY:
        log("错误: AGNES_API_KEY 未配置")
        return

    success_count = 0
    for i, img in enumerate(IMAGES):
        log(f"\n[{i+1}/{len(IMAGES)}] {img['label']}")
        output_path = OUTPUT_DIR / img['filename']
        if output_path.exists():
            log(f"  已存在，跳过: {output_path}")
            success_count += 1
            continue
        log(f"  prompt长度: {len(img['prompt'])} chars | size: {img.get('size', DEFAULT_SIZE)}")
        ok = generate_image(img['prompt'], output_path, size=img.get('size', DEFAULT_SIZE))
        if ok:
            success_count += 1

    log("\n" + "=" * 50)
    log(f"结果: {success_count}/{len(IMAGES)} 成功")
    log("=" * 50)

if __name__ == "__main__":
    main()
