    if not API_KEY:
        log("错误: AGNES_API_KEY 未配置")
        return

    success_count = 0
    for i, img in enumerate(IMAGES):
        log(f"\n[{i+1}/{len(IMAGES)}] {img['label']}")
        output_path = OUTPUT_DIR / img['filename']
        if output_path.exists():
            log(f"  已存在，跳过: {output_path}")
            success_count += 1
            continue
        log(f"  prompt长度: {len(img['prompt'])} chars | size: {img.get('size', DEFAULT_SIZE)}")
        ok = generate_image(img['prompt'], output_path, size=img.get('size', DEFAULT_SIZE))
        if ok:
            success_count += 1

    log("\n" + "=" * 50)
    log(f"结果: {success_count}/{len(IMAGES)} 成功")
    log("=" * 50)


