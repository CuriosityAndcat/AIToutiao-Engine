def generate_image(prompt: str, output_path: Path, size: str = DEFAULT_SIZE, retries: int = MAX_RETRIES) -> bool:
    """调用 Agnes API 生成图片"""
    url = f"{API_BASE}/images/generations"
    headers = {
        "Content-Type": "application/json",
        "Authorization": f"Bearer {API_KEY}"
    }
    payload = {
        "model": MODEL,
        "prompt": prompt,
        "size": size,
        "n": 1
    }

    for attempt in range(1, retries + 1):
        try:
            log(f"  请求... (尝试 {attempt}/{retries})")
            resp = requests.post(url, headers=headers, json=payload, timeout=TIMEOUT)
            log(f"  状态码: {resp.status_code}")
            resp.raise_for_status()
            data = resp.json()

            image_url = (
                data.get("data", [{}])[0].get("url")
                or data.get("url")
                or data.get("image_url")
            )
            if not image_url:
                log(f"  响应无URL: {json.dumps(data, ensure_ascii=False)[:200]}")
                continue

            log(f"  下载图片...")
            img_resp = requests.get(image_url, timeout=120)
            img_resp.raise_for_status()

            output_path.parent.mkdir(parents=True, exist_ok=True)
            output_path.write_bytes(img_resp.content)

            file_size = output_path.stat().st_size
            if file_size < 1024:
                log(f"  文件过小 ({file_size} bytes)")
                continue

            log(f"  成功! ({file_size / 1024:.1f} KB)")
            return True

        except Exception as e:
            log(f"  失败: {e}")
            if attempt < retries:
                wait = 2 ** attempt
                log(f"  等待 {wait}s...")
                time.sleep(wait)
            else:
                log(f"  重试耗尽")

    return False


